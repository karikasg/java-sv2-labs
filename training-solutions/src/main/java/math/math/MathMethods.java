package math.math;

public class MathMethods {

    public static void main(String[] args) {
        System.out.println(Math.max(12, 23));
        System.out.println(Math.min(12, 23));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(6.872635));
        System.out.println(Math.abs(23.75));
        System.out.println(Math.abs(-23.75));
        System.out.println(Math.negateExact(1));
        System.out.println(Math.addExact(23, 54));
        System.out.println(Math.subtractExact(54, 23));
        System.out.println(Math.multiplyExact(54, 23));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.incrementExact(25));
        System.out.println(Math.decrementExact(25));
        System.out.println(Math.random());
    }
}
